<template>
  <transition name="slide-up">
    <div class="setting-wrapper" v-show="menuVisible && settingVisible === 2">
      <div class="setting-progress">
        <div class="read-time-wrapper">
          <span class="read-time-text">{{getReadTime()}}</span>
        </div>
        <div class="progress-wrapper">
          <div class="progress-icon-wrapper" @click="prevSection">
            <span class="icon-back"></span>
          </div>
          <input type="range" class="progress"
            ref="progress"
            min="0"
            step="1"
            @input="onProgressInput($event.target.value)"
            @change="onProgressChange($event.target.value)"
            :value="readingProgress"
            :disabled="!bookAvailable"
          >
          <div class="progress-icon-wrapper" @click="nextSection">
            <span class="icon-forward"></span>
          </div>
        </div>
        <div class="text-wrapper">
          <span class="progress-section-text" v-if="bookAvailable">已读</span>
          <span class="progress-text">({{bookAvailable ? readingProgress + '%' : $t('book.loading')}})</span>
        </div>
      </div>
    </div>
  </transition>
</template>

<script>
import { setReadingProgressStorage } from '../../utils/localStorage'
import { ebookMixin } from '../../utils/mixin'
export default {
  mixins: [ebookMixin],
  data () {
    return {
      isProgressLoading: false
    }
  },
  methods: {
    onProgressInput (val) {
      this.set_readingProgress(val).then(() => {
        this.updateProgressBg(val)
      })
    },
    onProgressChange (val) {
      this.set_readingProgress(val).then(() => {
        this.displayProgress()
        this.updateProgressBg(val)
        setReadingProgressStorage(this.fileName, val)
      })
    },
    prevSection () {
      if (this.section > 0 && !this.isProgressLoading) {
        this.isProgressLoading = true
        this.set_section(this.section - 1).then(() => {
          this.displaySection(() => {
            this.isProgressLoading = false
            this.updateProgressBg()
          })
        })
      }
    },
    nextSection () {
      if (this.currentBook.spine.length - 1 > this.section && !this.isProgressLoading) {
        this.isProgressLoading = true
        this.set_section(this.section + 1).then(() => {
          this.displaySection(() => {
            this.isProgressLoading = false
            this.updateProgressBg()
          })
        })
      }
    },
    updateProgressBg () {
      this.$refs.progress.style.backgroundSize = this.readingProgress + '% 100%'
    }
  }
}
</script>

